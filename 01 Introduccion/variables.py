# variables.py
# Este script muestra cómo declarar y usar variables en Python.

# Declaración de variables
nombre = "Juan"
edad = 25
es_estudiante = True
promedio = 4.5

# Imprimir las variables
print("Nombre:", nombre)
print("Edad:", edad)
print("¿Es estudiante?:", es_estudiante)
print("Promedio:", promedio)

# Actualización de valores
edad += 1
promedio = (promedio + 5) / 2

# Imprimir los nuevos valores
print("\nValores actualizados:")
print("Nueva edad:", edad)
print("Nuevo promedio:", promedio)
